package com.example.frip.Pager2;

public class Pager2DTO {
    private int imgv2;
    private String tv2;

    public Pager2DTO(int imgv2, String tv2) {
        this.imgv2 = imgv2;
        this.tv2 = tv2;
    }


    public String getTv2() {
        return tv2;
    }

    public void setTv2(String tv2) {
        this.tv2 = tv2;
    }


    public int getImgv2() {
        return imgv2;
    }

    public void setImgv2(int imgv2) {
        this.imgv2 = imgv2;
    }
}
